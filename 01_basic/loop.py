"""
# 반복문 (Loop)

## 📌 개념
- 같은 코드를 여러 번 실행하는 구조

## 📌 종류
- for문: 정해진 횟수 반복
- while문: 조건이 참(True)인 동안 반복
"""

# =========================
# 1️⃣ for문 기본
# =========================
for i in range(5):
    print(i)  # 0 ~ 4 출력


# =========================
# 2️⃣ 리스트 반복
# =========================
fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)


# =========================
# 3️⃣ while문 기본
# =========================
i = 0

while i < 5:
    print(i)
    i += 1  # 무한루프 방지


# =========================
# 4️⃣ break (반복 중단)
# =========================
for i in range(10):
    if i == 5:
        break
    print(i)


# =========================
# 5️⃣ continue (건너뛰기)
# =========================
for i in range(5):
    if i == 2:
        continue
    print(i)


# =========================
# 6️⃣ 주의할 점
# =========================

